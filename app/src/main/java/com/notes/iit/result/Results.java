package com.notes.iit.result;

public class Results {

    public String name;
    public String roll;
    public String exam;
    public int year;
    public String cgpa;
    public String position;

    public Results(String name, String roll, String exam, int year, String cgpa, String position) {
        this.name = name;
        this.roll = roll;
        this.exam = exam;
        this.year = year;
        this.cgpa = cgpa;
        this.position = position;
    }
}
