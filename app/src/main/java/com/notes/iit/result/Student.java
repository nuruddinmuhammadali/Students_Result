package com.notes.iit.result;

import java.util.Date;

public class Student {

    public String name;
    public String roll;
    public String clas;
    public String batch;
    public String dept;
    public String address;

    public Student(String name, String roll, String clas, String batch, String dept, String address) {
        this.name = name;
        this.roll = roll;
        this.clas = clas;
        this.batch = batch;
        this.dept = dept;
        this.address = address;
    }
}
